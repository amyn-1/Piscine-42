#include <unistd.h>

void	ft_is_negative(int n)
{
	char	po;
	char	ne;

	po = 'P';
	ne = 'N';
	if (n < 0)
	{
		write(1, &ne, 1);
	}
	else
	{
		write(1, &po, 1);
	}
}
